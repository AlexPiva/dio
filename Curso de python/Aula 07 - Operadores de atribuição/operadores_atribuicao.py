# atribuição simples
saldo = 500
print(f'saldo = {saldo}')

# atribuição com adição
saldo += 200
print(f'saldo = {saldo}')

# atribuição com subtração
saldo -= 300
print(f'saldo = {saldo}')

# atribuição com multiplicação
saldo *= 2
print(f'saldo = {saldo}')

# atribuição com divisão
saldo /= 2
print(f'saldo = {saldo}')

# atribuição com divisão inteira
saldo //= 2
print(f'saldo = {saldo}')

# atribuição com módulo
saldo %= 180
print(f'saldo = {saldo}')

# atribuição com potência
saldo **= 2
print(f'saldo = {saldo}')