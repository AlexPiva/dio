# Inteiro para Float
preco = 10
print(preco)
preco = float(preco)
print(preco)
preco = preco / 2
print(preco)
print()

#Float para Inteiro
preco = 10.0
print(preco)
preco = int(preco)
print(preco)
preco = preco / 2
print(int(preco))
print()

# Conversão por divisao
preco = 10.0
print(preco)
print(preco / 2)
print(preco // 2)
print(100 // 2)
print()

# Numérico para String
preco = 10.50
idade = 52
print(str(preco))
print(str(idade))
texto = f'Idade {idade} e preço = {preco}'
print(texto)
print()

# String para Numérico
preco = "10.50"
print(float(preco))
idade = '28'
print(int(idade))